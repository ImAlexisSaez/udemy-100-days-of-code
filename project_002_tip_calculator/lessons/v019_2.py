# Data types

# String

print("Hello"[0])  # H
print("Hello"[4])  # o
print("Hello"[-1])  # o

# "123"  # Es un texto, no un número

print("123" + "345")  # Concatena, no suma.

# Integer

print(123 + 345)  # Suma, no concatena. Ahora son números, no texto.

print(123_456_789)  # _ espacios que no se tienen en cuenta en números.

# Float

print(3.141592)

# Boolean

True
False
